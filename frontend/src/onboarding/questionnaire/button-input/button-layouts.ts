export function getQuestionButtonPlacement(
  questionId: string,
  optionIndex: number,
  device: DeviceType
): QuestionButtonPlacement {
  return (
    QUESTION_BUTTON_SLOTS[questionId][device][optionIndex] ??
    DEFAULT_BUTTON_SLOTS[device][optionIndex % DEFAULT_BUTTON_SLOTS[device].length]
  );
}